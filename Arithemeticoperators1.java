package Myjava;

public class Arithemeticoperators1 {

			public static void main(String[] args) {
			    //first number
				int num1=17;
				//second number
				int num2=25;
				
			//modulus operation
		    int mod=num1%num2; 
			//display the result
			System.out.println("modulus of 17 and 25: "+ mod);
			//division operation
			int div=num1/num2;
			//display the result
			System.out.println("division of 17 and 25:"+div);
			//addition operation
			int add=num1+num2;
			System.out.println("addition of 17 and 25:"+ add);
			//subtraction operation
			int sub=num2-num1;
			System.out.println("subtraction of 17 and 25:"+ sub);

			}
	

}
